public class Circle {
    //java文件名必须与共有类保持一致
    public static void main(String args[]){
        //引用变量
        Circle x;
        //构造一个对象，并为其分配一个空间
        //引用变量的值是一个地址，而非一个具体的值
        x = new Circle();
        System.out.println(x);

    }


}
